int main() {

  float x, y;

  LCD.Clear(FEHLCD::Black);
  LCD.SetFontColor(FEHLCD::White);
  LCD.SetOrientation(FEHLCD::East);

  enum Direction dir = East; // Player Direction

  Dot dgents[MAP_WIDTH*MAP_HEIGHT];
  Tile ments[MAP_WIDTH*MAP_HEIGHT];

  Collider paths(path_data, 8, MAP_WIDTH, MAP_HEIGHT);

  int di = 0;
  int ti = 0;
  bool big = false;
  Dot dot;
  Tile tile;

  for (int x = 0; x < MAP_WIDTH; x++) {
    for (int y = 0; y < MAP_HEIGHT; y++) {
      if (path_data[y*MAP_WIDTH + x] == 1) {
        dot = Dot(x*8, y*8, big);
        dgents[di] = dot;
        di++;
      }
    }
  }

  for (int x = 0; x < MAP_WIDTH; x++) {
    for (int y = 0; y < MAP_HEIGHT; y++) {
      unsigned int block = map[y*MAP_WIDTH + x];
      TileType type = TileType(block / 10 % 10);
      Rotation rot;
      unsigned int rotate = block % 10;
      switch (rotate) {
        case 0:
          rot = Deg0;
          break;
        case 1:
          rot = Deg90;
          break;
        case 2:
          rot = Deg180;
          break;
        case 3:
          rot = Deg270;
          break;
        default:
          rot = Deg0;
      }
      tile = Tile(x*8, y*8, type, rot);
      ments[ti] = tile;
      ti++;
    }
  }

  Player player(&paths, 5, 0);
  Collider coll = Collider(path_data, 8, 28, 31);
  Player player(NULL, 5, 0);
  Ghost *ghosts = (Ghost *) calloc(4, sizeof(Ghost));
  
  // TODO : Give Real Positions on Grid
  ghosts[0] = Ghost(&coll, &player, 0, 16, 16);
  ghosts[1] = Ghost(&coll, &player, 1, 32, 16);
  ghosts[2] = Ghost(&coll, &player, 2, 16, 32);
  ghosts[3] = Ghost(&coll, &player, 3, 32, 32);

  unsigned long long frame = 0;

  while (!LCD.Touch(&x, &y));

  for (int i = 0; i < DG_COUNT; i++) {
    dgents[i].render();
  }

  while (true) {

    // Choose Direction for Player
    if (LCD.Touch(&x, &y))
    {
      if (y < 90)
      {
        dir = West;
      }
      else if (y > 160)
      {
        dir = East;
      }
      else if (x < 100)
      {
        dir = South;
      }
      else if (x > 200)
      {
        dir = North;
      }
    }

    for (int i = 0; i < DG_COUNT; i++) {
      dgents[i].render();
    }

    for (int i = 0; i < MAP_WIDTH*MAP_HEIGHT; i++) {
      ments[i].render();
    }

    // Move Entities
    player.move(dir);
    player.update(frame);
    player.render();

    for (int i = 0; i < 4; i++) {
      ghosts[i].move();
      ghosts[i].update(frame);
      ghosts[i].render();
    }

    frame++;
    Sleep(15);
  }
  return 0;
}